//Функции для integer
#pragma GCC diagnostic ignored "-Wimplicit-function-declaration"
#pragma GCC diagnostic ignored "-Wbuiltin-declaration-mismatch"
int squaring(int num)
{
    return (num*num);
}
double square_root(int num)
{
    return (sqrt(num));
}

//функции для double
double double_squaring(double num)
{
    return (num*num);
}
double double_square_root(double num)
{
    return (sqrt(num));
}

//функции для float
double float_squaring(float num)
{
    return (num*num);
}
double float_square_root(float num)
{
    return (sqrt(num));
}